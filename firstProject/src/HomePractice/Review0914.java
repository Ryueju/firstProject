package HomePractice;


// 변수 복습
// 대명사 같은 역할이라고 생각하자
//
public class Review0914 {

	
	public static void main(String[] args) {
		int a;
		//변수를 만드는 구문
		//int : 숫자 형식의 데이터 타입 , a : 변수의 이름
		a = 1;
		//a 라는 변수에 1이라는 값을 대입한 것
		
		System.out.println(a + 1); //2가 출력됨
		
		a = 2;
		// 변수를 재할당했음
		
		System.out.println(a + 1); //3이 출력됨
		
		
	}
}
